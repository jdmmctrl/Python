# Variables

my_string_variable = "My String Variable"
print(my_string_variable)

my_int_variable = 10
print(my_int_variable)

my_float_variable = 10.5
print(my_float_variable)

my_boolean_variable = True
print(my_boolean_variable)

print(my_string_variable, my_int_variable,
      my_float_variable, my_boolean_variable)

print(my_string_variable, str(my_int_variable),
      my_float_variable, my_boolean_variable)

my_list_variable = [1, 2, 3, 4, 5]
print(my_list_variable)

my_tuple_variable = (1, 2, 3, 4, 5)
print(my_tuple_variable)

my_dictionary_variable = {"key1": "value1", "key2": "value2"}
print(my_dictionary_variable)

my_set_variable = {1, 2, 3, 4, 5}
print(my_set_variable)

my_none_variable = None
print(my_none_variable)
