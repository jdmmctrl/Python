# Tipos de datos
# str, int, float, complex, bool, list, tuple, set, dict

print("Hola Mundo")
print(type("Hola Mundo"))  # Tipo de dato 'str'
print(type(1))  # Tipo de dato 'int'
print(type(1.0))  # Tipo de dato 'float'
print(type(1+2j))  # Tipo de dato 'complex'
print(type(True))  # Tipo de dato 'bool'
print(type([1, 2, 3]))  # Tipo de dato 'list'
print(type((1, 2, 3)))  # Tipo de dato 'tuple'
print(type({1, 2, 3}))  # Tipo de dato 'set'
print(type({'a': 1, 'b': 2}))  # Tipo de dato 'dict'
